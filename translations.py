def t(lang_code: str, key: str, **kwargs) -> str:
    """Get a translated string. Falls back to English if key/lang missing."""
    lang = TRANSLATIONS.get(lang_code, TRANSLATIONS["en"])
    text = lang.get(key, TRANSLATIONS["en"].get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text


def service_label(lang_code: str, service_key: str) -> str:
    lang = TRANSLATIONS.get(lang_code, TRANSLATIONS["en"])
    return lang.get("service_types", {}).get(
        service_key,
        TRANSLATIONS["en"]["service_types"].get(service_key, service_key),
    )
